#include "100-operations.h"
/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - div two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
*/
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
*/
int mod(int a, int b)
{
	return (a % b);
}
